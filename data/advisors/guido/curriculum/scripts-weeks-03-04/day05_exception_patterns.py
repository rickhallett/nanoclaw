# LESSON: EAFP vs LBYL
# ============================================================================

print()
print("=" * 60)
print("CHECKPOINT 4: EAFP vs LBYL — the Python way")
print("=" * 60)
print()
print("LBYL = Look Before You Leap (check first, then act)")
print("EAFP = Easier to Ask Forgiveness than Permission (try, catch if fails)")
print()
print("TRY:")
print()
print("  d = {'name': 'kai', 'role': 'sre'}")
print()
print("  # LBYL (un-Pythonic):")
print("  if 'age' in d:")
print("      age = d['age']")
print("  else:")
print("      age = 'unknown'")
print("  print(f'LBYL: {age}')")
print()
print("  # EAFP (Pythonic):")
print("  try:")
print("      age = d['age']")
print("  except KeyError:")
print("      age = 'unknown'")
print("  print(f'EAFP: {age}')")
print()
print("  # Even more Pythonic: d.get('age', 'unknown')")
print("  # But the principle matters for more complex cases:")
print()
print("  # LBYL (race condition in concurrent code):")
print("  import os")
print("  path = '/tmp/somefile'")
print("  if os.path.exists(path):  # file could vanish between check and open")
print("      with open(path) as f:")
print("          data = f.read()")
print()
print("  # EAFP (no race condition):")
print("  try:")
print("      with open(path) as f:")
print("          data = f.read()")
print("  except FileNotFoundError:")
print("      data = None")
print()
print("EAFP is the Python way. It's also safer for concurrent systems.")
print()

embed(header="Checkpoint 4: EAFP — try first, ask questions later")

