print("=" * 60)
print("CHECKPOINT 2: Parsing columnar output")
print("=" * 60)
print()
print("Commands like df, ps, mount produce columnar output with headers.")
print("Pattern: skip header, split each line on whitespace.")
print()
print("TRY:")
print("  r = subprocess.run(['df', '-h'], capture_output=True, text=True)")
print("  lines = r.stdout.strip().split('\\n')")
print("  header = lines[0]")
print("  print(f'Header: {header}')")
print("  print()")
print()
print("  # Parse each data line")
print("  for line in lines[1:]:")
print("      parts = line.split()")
print("      if len(parts) >= 5:")
print("          filesystem = parts[0]")
print("          usage_pct = parts[4]    # e.g., '45%'")
print("          mount = parts[-1]       # last field, handles spaces in earlier fields")
print("          print(f'  {mount}: {usage_pct} ({filesystem})')")
print()
print("  # split() with no args splits on ANY whitespace and drops empties.")
print("  # split(None, N) splits into at most N+1 parts — useful for ps aux")
print("  # where the command field can contain spaces.")
print()

