# ============================================================================
# LESSON: shell=True — when and why (almost never)
# ============================================================================

print()
print("=" * 60)
print("CHECKPOINT 3: shell=True — understand it, avoid it")
print("=" * 60)
print()
print("shell=True passes the command to /bin/sh as a single string.")
print("This enables pipes, globs, and variable expansion.")
print()
print("TRY:")
print("  # With shell=True: command is a STRING, not a list")
print("  r = subprocess.run('echo $HOME', shell=True, capture_output=True, text=True)")
print("  print(r.stdout.strip())  — expands $HOME")
print()
print("  # Piping works")
print("  r = subprocess.run('ls /usr/bin | wc -l', shell=True,")
print("                     capture_output=True, text=True)")
print("  print(f'Binaries: {r.stdout.strip()}')")
print()
print("  # Globbing works")
print("  r = subprocess.run('ls /tmp/*.py 2>/dev/null || echo none',")
print("                     shell=True, capture_output=True, text=True)")
print("  print(r.stdout.strip())")
print()
print("WHY TO AVOID IT:")
print("  # Shell injection — NEVER use shell=True with user input:")
print("  user_input = 'hello; rm -rf /'  # malicious")
print("  # subprocess.run(f'echo {user_input}', shell=True)  # EXECUTES rm -rf /")
print()
print("RULE: if the command string is hardcoded, shell=True is acceptable.")
print("If ANY part comes from user input, variables, or config: use a list.")
print()

embed(header="Checkpoint 3: shell=True — try it, understand it, avoid it")

# ============================================================================
# LESSON: cmd_ok helper and common idioms
# ============================================================================

print()
print("=" * 60)
print("CHECKPOINT 4: Useful idioms")
print("=" * 60)
print()
print("TRY THESE — type them out:")
print()
print("  # Idiom 1: check if a command exists")
print("  def cmd_exists(name):")
print("      try:")
print("          subprocess.run(['which', name], capture_output=True, check=True)")
print("          return True")
print("      except (subprocess.CalledProcessError, FileNotFoundError):")
print("          return False")
print()
print("  print(cmd_exists('python3'))   # True")
print("  print(cmd_exists('kubectl'))   # depends on your system")
print("  print(cmd_exists('fakecmd'))   # False")
print()
print("  # Idiom 2: get single-value output")
print("  def cmd_output(cmd):")
print("      r = subprocess.run(cmd, capture_output=True, text=True, check=True)")
print("      return r.stdout.strip()")
print()
print("  print(cmd_output(['hostname']))")
print("  print(cmd_output(['whoami']))")
print("  print(cmd_output(['uname', '-r']))")
print()
print("  # Idiom 3: run or default")
print("  def cmd_or(cmd, default='unknown'):")
print("      try:")
print("          return cmd_output(cmd)")
print("      except Exception:")
print("          return default")
print()
print("  print(cmd_or(['hostname']))")
print("  print(cmd_or(['fakecmd'], default='n/a'))")
print()

