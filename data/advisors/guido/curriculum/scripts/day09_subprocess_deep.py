# ============================================================================
# LESSON: Processing multi-line output
# ============================================================================

print("=" * 60)
print("CHECKPOINT 1: Parsing multi-line output cleanly")
print("=" * 60)
print()
print("Most commands return multi-line output. The pattern:")
print("  result.stdout.strip().split('\\n')")
print()
print("TRY:")
print("  r = subprocess.run(['ls', '/usr/bin'], capture_output=True, text=True)")
print("  lines = r.stdout.strip().split('\\n')")
print("  print(f'Total: {len(lines)} binaries')")
print("  print(f'First 5: {lines[:5]}')")
print("  print(f'Last 5: {lines[-5:]}')")
print()
print("  # Filter: find all that start with 'python'")
print("  py = [l for l in lines if l.startswith('python')]")
print("  print(f'Python binaries: {py}')")
print()
print("  # Common gotcha: empty string after split on trailing newline")
print("  raw = 'a\\nb\\nc\\n'")
print("  print(raw.split('\\n'))      — ['a', 'b', 'c', ''] ← trailing empty")
print("  print(raw.strip().split('\\n'))  — ['a', 'b', 'c']  ← clean")
print()
print("ALWAYS .strip() before .split('\\n'). The trailing newline creates ghosts.")
print()

embed(header="Checkpoint 1: multi-line output — strip then split")

