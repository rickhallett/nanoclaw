print("              report[name] = {'status': 'not_found', 'error': f'{cmd[0]} not found'}")
print("          except subprocess.TimeoutExpired:")
print("              report[name] = {'status': 'timeout', 'error': f'>{timeout}s'}")
print("      return report")
