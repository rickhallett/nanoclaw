print("TRY:")
print()
print("  # 1. Always create in UTC")
print("  now = datetime.now(timezone.utc)")
print()
print("  # 2. Store as ISO string")
print("  stored = now.isoformat()")
print("  print(f'stored: {stored}')")
print()
print("  # 3. Parse back")
print("  parsed = datetime.fromisoformat(stored)")
print("  print(f'parsed: {parsed}')")
print("  print(f'tzinfo: {parsed.tzinfo}')  — UTC preserved!")
print()
print("  # 4. Display in local time")
print("  bst = timezone(timedelta(hours=1))")
print("  display = parsed.astimezone(bst)")
print("  print(f'display: {display.strftime(\"%d %b %Y %H:%M %Z\")}')")
print()
print("  # Making a naive datetime aware (when you KNOW its timezone)")
print("  naive = datetime(2026, 4, 8, 14, 30, 0)")
print("  aware = naive.replace(tzinfo=timezone.utc)  # attach UTC")
print("  print(aware)")
print()
print("  # WARNING: replace() does NOT convert. It just stamps.")
print("  # If the naive datetime was actually in BST, you'd do:")
print("  bst_aware = naive.replace(tzinfo=bst)")
print("  utc_version = bst_aware.astimezone(timezone.utc)")
print("  print(f'BST 14:30 = UTC {utc_version.strftime(\"%H:%M\")}')")
