print()
print("=" * 60)
print("CHECKPOINT 4: timedelta — durations and comparisons")
print("=" * 60)
print()
print("TRY:")
print()
print("  # Creating timedeltas")
print("  d1 = timedelta(days=1)")
print("  d2 = timedelta(hours=36)")
print("  d3 = timedelta(weeks=2)")
print("  d4 = timedelta(days=1, hours=6, minutes=30)")
print()
print("  # Normalisation: timedelta stores as (days, seconds, microseconds)")
print("  print(d2)        — 1 day, 12:00:00 (36 hours normalised)")
print("  print(d2.days)   — 1")
print("  print(d2.seconds) — 43200 (the remaining 12 hours in seconds)")
print("  print(d2.total_seconds()) — 129600.0 (all of it)")
print()
print("  # GOTCHA: .seconds is NOT total seconds!")
print("  weird = timedelta(days=2, hours=3)")
print("  print(weird.seconds)         — 10800 (just the 3 hours)")
print("  print(weird.total_seconds()) — 183600 (2 days + 3 hours)")
print("  # ALWAYS use .total_seconds() for the full duration")
print()
print("  # Arithmetic between timedeltas")
print("  print(d1 + d2)      — 2 days, 12:00:00")
print("  print(d3 - d1)      — 13 days")
print("  print(d1 * 3)       — 3 days")
print("  print(d3 / d1)      — 14.0 (how many d1s in d3)")
print()
print("  # Comparison")
print("  print(d1 < d2)      — True (1 day < 36 hours)")
print("  print(d3 > d2)      — True")
print()

