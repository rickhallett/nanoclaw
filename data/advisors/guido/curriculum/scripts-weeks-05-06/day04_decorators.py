print("          return fn(*args, **kwargs)")
print("      return wrapper")
print()
print("  @good_decorator")
print("  def my_func2():")
print("      '''My docstring.'''")
print("      pass")
print()
print("  print(my_func2.__name__)  # 'my_func2' — correct")
print("  print(my_func2.__doc__)   # 'My docstring.' — preserved")
print()
print("RULE: ALWAYS use @wraps(fn) in your wrapper. No exceptions.")
