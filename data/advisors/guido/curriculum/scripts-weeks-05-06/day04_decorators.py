print()
print("=" * 60)
print("CHECKPOINT 4: Decorators that take arguments")
print("=" * 60)
print()
print("A decorator factory is a function that RETURNS a decorator.")
print("Three levels of nesting: factory → decorator → wrapper.")
print()
print("TRY:")
print()
print("  def retry(max_attempts=3, delay=0.1):")
print("      def decorator(fn):")
print("          @wraps(fn)")
print("          def wrapper(*args, **kwargs):")
print("              for attempt in range(1, max_attempts + 1):")
print("                  try:")
print("                      return fn(*args, **kwargs)")
print("                  except Exception as e:")
print("                      if attempt == max_attempts:")
print("                          raise")
print("                      print(f'  {fn.__name__} attempt {attempt} failed: {e}')")
print("                      time.sleep(delay)")
print("          return wrapper")
print("      return decorator")
print()
print("  import random")
print("  @retry(max_attempts=5, delay=0.05)")
print("  def flaky_api():")
print("      if random.random() < 0.7:")
print("          raise ConnectionError('timeout')")
print("      return 'success'")
print()
print("  try:")
print("      result = flaky_api()")
print("      print(f'got: {result}')")
print("  except ConnectionError:")
print("      print('all attempts failed')")
print()

