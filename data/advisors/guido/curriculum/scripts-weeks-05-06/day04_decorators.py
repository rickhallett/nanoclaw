# ============================================================================
# LESSON: A decorator is just a function that returns a function
# ============================================================================

print("=" * 60)
print("CHECKPOINT 1: Decorators are syntactic sugar for closures")
print("=" * 60)
print()
print("TRY:")
print()
print("  # Without decorator syntax:")
print("  def shout(fn):")
print("      def wrapper(*args, **kwargs):")
print("          result = fn(*args, **kwargs)")
print("          return result.upper()")
print("      return wrapper")
print()
print("  def greet(name):")
print("      return f'hello {name}'")
print()
print("  greet = shout(greet)  # this IS what @ does")
print("  print(greet('kai'))")
print()
print("  # With decorator syntax (identical):")
print("  @shout")
print("  def greet2(name):")
print("      return f'hello {name}'")
print()
print("  print(greet2('kai'))")
print()
print("  # @shout is EXACTLY equivalent to: greet2 = shout(greet2)")
print("  # That's all it is. A function wrapping another function.")
print()

embed(header="Checkpoint 1: @ is just function = decorator(function)")

