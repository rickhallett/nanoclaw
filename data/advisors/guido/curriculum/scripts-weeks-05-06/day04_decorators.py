print()
print("TYPE EACH ONE OUT:")
print()
print("  # 1. Logging decorator")
print("  import logging")
print("  def log_calls(fn):")
print("      @wraps(fn)")
print("      def wrapper(*args, **kwargs):")
print("          logging.info('Calling %s(%s, %s)', fn.__name__, args, kwargs)")
print("          result = fn(*args, **kwargs)")
print("          logging.info('%s returned %s', fn.__name__, result)")
print("          return result")
print("      return wrapper")
print()
print("  # 2. Cache with TTL")
print("  def cache_ttl(seconds=60):")
print("      def decorator(fn):")
print("          _cache = {}")
print("          @wraps(fn)")
print("          def wrapper(*args):")
print("              now = time.time()")
print("              if args in _cache:")
print("                  result, timestamp = _cache[args]")
print("                  if now - timestamp < seconds:")
print("                      return result")
print("              result = fn(*args)")
print("              _cache[args] = (result, now)")
print("              return result")
print("          return wrapper")
print("      return decorator")
print()
print("  @cache_ttl(seconds=2)")
print("  def expensive(n):")
print("      print(f'  computing for {n}...')")
print("      return n * n")
print()
print("  print(expensive(5))  # computes")
print("  print(expensive(5))  # cached")
print("  time.sleep(2.1)")
print("  print(expensive(5))  # recomputes (TTL expired)")
print()

