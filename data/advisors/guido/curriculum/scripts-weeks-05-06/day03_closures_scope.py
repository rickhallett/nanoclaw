print("=" * 60)
print()
print("This is the question you couldn't answer. Study it carefully.")
print()
print("TRY:")
print()
print("  def make_multipliers():")
print("      return [lambda x: x * i for i in range(4)]")
print()
print("  fns = make_multipliers()")
print("  print([f(2) for f in fns])")
print("  # PREDICT: ?")
print()
print("  # ANSWER: [6, 6, 6, 6] — NOT [0, 2, 4, 6]")
print()
print("WHY:")
print("  All four lambdas capture the VARIABLE i, not its VALUE.")
print("  They don't evaluate i when they're created.")
print("  They evaluate i when they're CALLED.")
print("  By the time you call them, the loop is done and i = 3.")
print("  So every lambda computes x * 3.")
print()
print("  # Prove it:")
print("  fns = make_multipliers()")
print("  # The loop is done. i is 3.")
print("  for f in fns:")
print("      print(f.__closure__[0].cell_contents)  # all say 3")
print()
print("THE FIX — default argument captures the VALUE at creation time:")
print()
print("  def make_multipliers_fixed():")
print("      return [lambda x, i=i: x * i for i in range(4)]")
print()
print("  fns = make_multipliers_fixed()")
print("  print([f(2) for f in fns])")
print("  # [0, 2, 4, 6] — each lambda has its own i")
print()

embed(header="Checkpoint 3: THE TRAP — late binding. i=i is the fix.")

