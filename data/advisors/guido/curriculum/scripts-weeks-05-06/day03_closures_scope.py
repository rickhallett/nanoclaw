# LESSON: Common closure patterns
# ============================================================================

print()
print("=" * 60)
print("CHECKPOINT 5: Closure patterns for SRE")
print("=" * 60)
print()
print("TRY EACH:")
print()
print("  # Pattern 1: memoizer (simple cache)")
print("  def memoize(fn):")
print("      cache = {}")
print("      def wrapper(*args):")
print("          if args not in cache:")
print("              cache[args] = fn(*args)")
print("          return cache[args]")
print("      return wrapper")
print()
print("  @memoize")
print("  def slow_add(a, b):")
print("      print(f'  computing {a}+{b}...')")
print("      return a + b")
print()
print("  print(slow_add(1, 2))  # computes")
print("  print(slow_add(1, 2))  # cached!")
print("  print(slow_add(3, 4))  # computes")
print()
print("  # Pattern 2: rate limiter")
print("  import time")
print("  def rate_limit(min_interval):")
print("      last_call = [0]  # list so we can mutate without nonlocal")
print("      def decorator(fn):")
print("          def wrapper(*args, **kwargs):")
print("              elapsed = time.time() - last_call[0]")
print("              if elapsed < min_interval:")
print("                  print(f'  rate limited ({elapsed:.2f}s < {min_interval}s)')")
print("                  return None")
print("              last_call[0] = time.time()")
print("              return fn(*args, **kwargs)")
print("          return wrapper")
print("      return decorator")
print()
print("  @rate_limit(1.0)")
print("  def api_call():")
print("      return 'response'")
print()
print("  print(api_call())  # works")
print("  print(api_call())  # rate limited")
print("  time.sleep(1.1)")
print("  print(api_call())  # works again")
print()

