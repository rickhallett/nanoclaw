print()
print("  # glob patterns:")
print("  # *        — any characters (not /)")
print("  # **       — any path (recursive, rglob does this)")
print("  # ?        — single character")
print("  # [abc]    — character set")
print()
print("  # Find all Python files in current dir (not recursive)")
print("  py_files = list(Path.cwd().glob('*.py'))")
